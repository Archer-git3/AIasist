# ── Допоміжна функція ─────────────────────────────────────────────────────────

def extract_json(raw: str) -> dict:
    """Розпарсити JSON з відповіді моделі (обробляє ```json ... ``` огорожі)."""
    text = raw.strip()
    text = re.sub(r"^```(?:json)?\s*", "", text, flags=re.MULTILINE)
    text = re.sub(r"\s*```\s*$",       "", text, flags=re.MULTILINE)
    text = text.strip()

    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass

    start = text.find("{")
    if start != -1:
        depth = 0
        for i, ch in enumerate(text[start:], start):
            if ch == "{":
                depth += 1
            elif ch == "}":
                depth -= 1
                if depth == 0:
                    try:
                        return json.loads(text[start: i + 1])
                    except json.JSONDecodeError:
                        break

    raise ValueError(f"Не вдалося розпарсити JSON. Початок: {raw[:400]}")


# ── Головна функція аналізу ───────────────────────────────────────────────────

async def analyze_with_ollama(
    seller_text: str,
    buyer_text:  str,
    contract_details: any,
    case_docs:   dict | None = None,
) -> dict:
    """
    Провести аналіз справи: RAG-пошук + запит до Ollama.
    Повертає словник з висновком та метаданими RAG.
    """
    log.info("Починаємо аналіз з Ollama. Модель: %s", MODEL)

    # ── RAG ──────────────────────────────────────────────────────────────────
    def get_val(v):
        if isinstance(v, tuple) and len(v) > 0:
            return str(v[0])
        return str(v)

    case_docs_combined = " ".join(get_val(v).strip() for v in (case_docs or {}).values() if v)
    rag_context, rag_chunks = build_rag_context(seller_text, buyer_text, case_docs_combined)
    log.info("RAG: використано %d чанків з бази знань", len(rag_chunks))

    # ── Перевірка наявності графіку поставки ─────────────────────────────────
    has_schedule = case_docs and "schedule" in case_docs and case_docs["schedule"].strip()
    schedule_disclaimer = (
        "\nВАЖЛИВО: На даний момент інформація про графіки поставки відсутня "
        "через відсутність доступу до відповідних документів. "
        if not has_schedule else ""
    )
    if not has_schedule:
        log.warning("Графік поставки відсутній")

    # ── RAG-секція системного промпту ─────────────────────────────────────────
    rag_section = ""
    if rag_context:
        rag_section = (
            f"\n=== РЕЛЕВАНТНІ НОРМИ З БАЗИ ЗНАНЬ (витягнуто RAG) ===\n"
            f"Використовуй ТІЛЬКИ ці уривки при аналізі. Посилайся на конкретні пункти.\n"
            f"{rag_context}\n"
            f"=== КІНЕЦЬ БАЗИ ЗНАНЬ ===\n"
        )

    system_prompt = (
        "Ти — досвідчений юридичний аналітик та арбітр з України. "
        "Аналізуй спори об'єктивно на підставі наданих норм."
        + rag_section
    )

    # ── Документи справи ─────────────────────────────────────────────────────
    case_docs = case_docs or {}
    case_docs_parts = [
        v for v in [
            case_docs.get("contract",    ""),
            case_docs.get("schedule",    ""),
            case_docs.get("certificate", ""),
        ] if v.strip()
    ]
    # Динамічно додаємо всі інші ключі (з /analyze-case)
    standard_keys = {"contract", "schedule", "certificate"}
    for key, val in case_docs.items():
        if key not in standard_keys and val.strip():
            case_docs_parts.append(val)

    case_docs_section = ""
    if case_docs_parts:
        case_docs_section = (
            "═══ ПЕРВИННІ ДОКУМЕНТИ СПРАВИ ═══\n"
            "Це офіційні документи конкретного спору. Посилайся на них у ПЕРШУ ЧЕРГУ.\n\n"
            + "\n\n".join(case_docs_parts)
            + "\n═══ КІНЕЦЬ ДОКУМЕНТІВ СПРАВИ ═══\n\n"
        )
        log.debug("Додано %d блоків документів справи до промпту", len(case_docs_parts))

    contract_stat_block = f"""
        ────────────────────
        ДЕТАЛІ ВИКОНАННЯ КОНТРАКТУ (ОФІЦІЙНІ ДАНІ):
        - Номер договору: {contract_details.number}
        - Дата: {contract_details.date}
        - Сума контракту: {contract_details.totalCost} грн
        - План обсягу: {contract_details.totalVolume} куб.м
        - Фактично поставлено: {contract_details.deliveredVolume} куб.м
        - ВІДСОТОК ВИКОНАННЯ: {contract_details.executionPercent}%
        - ЗМІНА ЦІНИ: {contract_details.priceChangePercent}%
        ────────────────────
        
    """


    user_message = f"""
    {case_docs_section}
    {contract_stat_block}
    
            ────────────────────
            ПОЯСНЕННЯ ПРОДАВЦЯ:
            {seller_text}

            ────────────────────
            ПОЯСНЕННЯ ПОКУПЦЯ:
            {buyer_text}

            ────────────────────
            ЗАВДАННЯ:
            Проведи об'єктивний юридичний аналіз наданих матеріалів та сформуй офіційний "Висновок агента Біржі".

            ТИ ПОВИНЕН ПОВЕРНУТИ ВИКЛЮЧНО ТЕКСТ ВИСНОВКУ.

            🚨 БАЗОВИЙ ФОРМАТ ВІДПОВІДІ:
            У більшості випадків ти ПОВИНЕН використовувати ШАБЛОН, наведений нижче. Будь-яка несплата, затримка, відсутність повідомлень, невідібраний товар або скарга однієї зі сторін — це ПОРУШЕННЯ, які вимагають розпису за ШАБЛОНОМ.

            🚨 КРИТИЧНІ ПРАВИЛА ЗАПОВНЕННЯ (ОБОВ'ЯЗКОВО ДО ВИКОНАННЯ): 🚨
            1. ВІДСУТНІСТЬ ПОРУШЕНЬ: Якщо ОБИДВІ сторони виконали всі умови І не мають жодних претензій одна до одної — шаблон не використовується. Напиши: "Агент Біржі провів збір копій документів та пояснень. Надані докази прийняті. За результатами аналізу ознаки порушень виконання договору купівлі-продажу відсутні." На цьому заверши відповідь.
            2. ТІЛЬКИ ЧИСТИЙ ТЕКСТ (PLAIN TEXT): Категорично заборонено використовувати жирний шрифт (**), символи #, заголовки Markdown або будь-яке інше форматування. Весь текст має бути одного вигляду.
            3. ТІЛЬКИ ФАКТИЧНІ ДАНІ: Вставляй виключно ті цифри, дати, обсяги та суми, які є у вхідних текстах. Вигадувати дані заборонено.
            4. ГНУЧКІСТЬ ПРИ ВІДСУТНОСТІ ДАНИХ (ВАЖЛИВО): Якщо для певного блоку (наприклад, вимоги щодо об'ємів та термінів) повністю відсутня інформація, НЕ роби списків з багаторазовим повторенням "інформація відсутня". Замість цього згорни цей блок в одне природне речення. Наприклад: "Інформація щодо конкретних термінів та об'ємів, які мав надати Продавець, відсутня в матеріалах справи." Текст має звучати по-людськи та логічно.
            5. ВІДСУТНІСТЬ ПОЯСНЕНЬ: Якщо сторона не надала пояснень, пиши: "пояснень не надав".
            6. ЯКЩО укладена додатково угода то просто пишешь "Укладенна додаткова угода до ЧИСЛА_ЯКОГО_УКАДЕНА_УГОДА" і більше нічого не потрібно УНІКАЛЬНИЙ ВИПАДОК
            7. ПЕРЕВИКОНАННЯ КОНТРАКТУ (УВАГА): Зверни увагу на блок "ДЕТАЛІ КОНТРАКТУ ТА СТАН ВИКОНАННЯ". Якщо відсоток виконання становить 100% або більше (наприклад, 104.58%) пишеш що контракт виконаний якщо немає інших предяв від одного з учасників  
           
            👇 ШАБЛОН ДЛЯ ГЕНЕРАЦІЇ ВІДПОВІДІ:
            (Заміни слова ВЕЛИКИМИ_ЛІТЕРАМИ на факти. Згортай пункти списку в одне речення, якщо фактичні дані відсутні )

            Агент Біржі провів збір копій, документів та пояснень від учасників торгів, та провів аналіз отриманої інформації, щодо виявлених ознак порушення з виконання договору купівлі-продажу.

            Встановлено:

            Продавець та Покупець погодили НАЗВА_ОСНОВНОГО_ДОКУМЕНТА, яку визначили в ЕТС Біржі.

            Умови:

            За НАЗВА_ОСНОВНОГО_ДОКУМЕНТА, Продавець та Покупець погодили наступні партії поставки:
            - Термін: ПЕРІОД_ПОСТАВКИ_АБО_ДАТА
            - Об'єм: ТОЧНИЙ_ОБЄМ_ТОВАРУ на суму: ТОЧНА_СУМА_У_ГРН

            Продавець у письмовому вигляді повідомив Покупця про готовність партії та надсилання запрошення для відбору партії товару СПОСІБ_ВІДПРАВКИ_ПОВІДОМЛЕННЯ.

            Відповідно до НАЗВА_ОСНОВНОГО_ДОКУМЕНТА, Продавець надав Покупцеві повідомлення про готовність партії та запрошення на відбір партії, а мав надати:
            - Термін: ПЕРІОД_ПОСТАВКИ
            - Об'єм: ТОЧНИЙ_ОБЄМ_ТОВАРУ на суму: ТОЧНА_СУМА_У_ГРН

            Покупець СТАТУС_НАДАННЯ_ПОВІДОМЛЕННЯ повідомлення про направлення представника для відбору продукції.

            Відповідно до НАЗВА_ОСНОВНОГО_ДОКУМЕНТА, Продавець письмово направив на електронну пошту Покупця рахунки на оплату партії товару.

            Відповідно до умов договору, Покупець мав оплатити рахунок протягом КІЛЬКІСТЬ_ДНІВ робочих днів після отримання рахунку на оплату партії товару. Покупець оплатив СТАТУС_ОПЛАТИ.

            Згідно з актом звірки залишок на користь Покупця становить: СУМА_ЗАЛИШКУ_АБО_ВІДСУТНІСТЬ, гарантійний внесок — СУМА_ГАРАНТІЙНОГО_ВНЕСКУ.

            Продавець у своєму поясненні зазначив, що КОРОТКИЙ_ЗМІСТ_ПОЯСНЕННЯ_ПРОДАВЦЯ.

            Покупець посилався на те, що КОРОТКИЙ_ЗМІСТ_ПОЯСНЕННЯ_ПОКУПЦЯ.

            Висновок/Рішення:

            Отже,
            Продавець:
            - Підписав НАЗВА_ОСНОВНОГО_ДОКУМЕНТА
            - Надав КІЛЬКІСТЬ_ПОВІДОМЛЕНЬ повідомлень про готовність партії товару та запрошення на відбір
            - СТАТУС_НАДАННЯ_РАХУНКІВ КІЛЬКІСТЬ_РАХУНКІВ рахунків.
            - Постачання партій: СТАТУС_ПОСТАЧАННЯ

            Покупець:
            - Підписав НАЗВА_ОСНОВНОГО_ДОКУМЕНТА
            - СТАТУС_НАДАННЯ_ПОВІДОМЛЕННЯ_ВІД_ПОКУПЦЯ повідомлення про направлення представника для відбору продукції.
            - Оплатив рахунки: СТАТУС_ОПЛАТИ_РАХУНКІВ
            - Посилався на: ПРИЧИНИ_НЕВИКОНАННЯ
            """

    payload = {
        "model": MODEL,
        "messages": [
            {"role": "system", "content": system_prompt},
            {"role": "user",   "content": user_message},
        ],
        "stream": False,
        "options": {
            "temperature": 0.00,
            "num_ctx":     16384,
            "num_predict": 4096,
        },
    }

    log.info("Надсилаємо запит до Ollama (%s)...", MODEL)
    async with httpx.AsyncClient(timeout=900.0) as client:
        resp = await client.post(f"{OLLAMA_URL}/api/chat", json=payload)
        resp.raise_for_status()
        content = resp.json()["message"]["content"]
    log.info("Відповідь від Ollama отримано (%d символів)", len(content))

    # ── Збираємо результат + метадані RAG ────────────────────────────────────
    result: dict = {"agent_conclusion": content.strip()}

    sources_map: dict[str, dict] = {}
    for c in rag_chunks:
        src = c["source"]
        if src not in sources_map or c["score"] > sources_map[src]["best_score"]:
            sources_map[src] = {
                "source":      src,
                "best_score":  round(c["score"], 3),
                "chunk_count": 0,
                "preview":     c["text"][:500].strip(),
            }
        sources_map[src]["chunk_count"] += 1

    result["_rag_sources"]     = list(sources_map.values())
    result["_rag_chunks_used"] = len(rag_chunks)
    result["_rag_files_used"]  = len(sources_map)

    log.info(
        "Аналіз завершено. RAG-файлів: %d, чанків: %d",
        result["_rag_files_used"], result["_rag_chunks_used"],
    )
    return result
